// // What is it?
// // LDBL_DIG is a macro in the C library of files that guarantees the decimal digits of precision in the long double data type.

// // What problem does it solve?
// LDBL_DIG solves the problem of determining how many decimal digits long double can store.

// Pros of LDBL_DIG
// 1. Guaranteed percision
// 2. Avoids precision errors

// Cons of LDBL_DIG
// 1. The number of decimal digit precision can vary depending on your platform
// 2. It only solves the problem of determining how many decimal digits long double can solve, and nothing else